# Программа, которая принимает текст пользовательского ввода и печатает его наоборот
message = str(input("Введите любой текст и выполучите его наоборот: "))
new_message = ""
for i in message[::-1]:
    new_message += i
print("А вот ваш новый текст:", new_message)
input("\n\nНажмите Enter, чтобы выйти из программы...")
